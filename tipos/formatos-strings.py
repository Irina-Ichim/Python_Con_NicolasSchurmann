"""
Este script muestra cómo crear un nombre completo a partir de un nombre y un apellido, 
y cómo crear una cadena de texto utilizando una f-string.
"""
nombre = "Irina"
apellido = "Ichim"
nombre_completo = nombre + " " + apellido
print(nombre_completo)
todo_nombre = f"{nombre[1]} {2 + 5}"
print(todo_nombre)